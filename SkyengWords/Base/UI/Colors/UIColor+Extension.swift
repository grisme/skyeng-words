import UIKit

extension UIColor {
    
    enum Common {
        
        enum View {
            static let viewBackground = UIColor(named: "viewBackground")!
            static let cellBackground = UIColor(named: "cellBackground")!
        }
        
        enum Text {
            static let titleText = UIColor(named: "titleText")!
            static let subtitleText = UIColor(named: "subtitleText")!
        }
        
    }
    
}
